import pytest
import epidemic


@pytest.mark.parametrize("tm,n,s0,i0,b,a,expected", [
    (18, 432, 1004, 1, 0.00209, 0.51, 420),
    (12, 288, 1007, 2, 0.00206, 0.45, 461),
    (13, 312, 999, 1, 0.00221, 0.55, 409),
    (24, 576, 1005, 1, 0.00216, 0.45, 474),
    (24, 576, 982, 1, 0.00214, 0.44, 460),
    (20, 480, 1000, 1, 0.00199, 0.53, 386),
    (28, 672, 980, 1, 0.00198, 0.44, 433),
    (14, 336, 996, 2, 0.00206, 0.41, 483),
    (13, 312, 993, 2, 0.0021, 0.51, 414),
    (28, 672, 999, 1, 0.00197, 0.55, 368),
])
def test_epidemic(tm, n, s0, i0, b, a, expected):
    assert abs(epidemic(tm, n, s0, i0, b, a) - expected) <= 1
